package produttoriConsumatori;

public class MyObject {
	
	private int propertyOne;
	private int propertyTwo;
	
	public MyObject(int o, int t){
		this.propertyOne = o;
		this.propertyTwo = t;
	}

	public int getPropertyOne() {
		return propertyOne;
	}

	public void setPropertyOne(int propertyOne) {
		this.propertyOne = propertyOne;
	}

	public int getPropertyTwo() {
		return propertyTwo;
	}

	public void setPropertyTwo(int propertyTwo) {
		this.propertyTwo = propertyTwo;
	}
	
	
}
